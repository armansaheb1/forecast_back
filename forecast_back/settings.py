def str_to_bool(value):
    """
    Convert string to boolean.
    Handles 'True', 'true', '1', 'yes', 'on' as True
    Handles 'False', 'false', '0', 'no', 'off', '' as False
    """
    if isinstance(value, bool):
        return value
    if isinstance(value, str):
        return value.lower() in ('true', '1', 'yes', 'on')
    return bool(value)


